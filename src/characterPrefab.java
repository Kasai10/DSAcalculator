public class characterPrefab {
    String name;
    int MU;
    int KL;
    int IN;
    int CH;
    int FF;
    int GE;
    int KO;
    int KK;

    characterPrefab(String name, int MU, int KL, int IN, int CH, int FF, int GE, int KO, int KK){
        this.name = name;
        this.MU = MU;
        this.KL = KL;
        this.CH = CH;
        this.FF = FF;
        this.GE = GE;
        this.KO = KO;
        this.KK = KK;
    }
}
